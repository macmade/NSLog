#import "ApplicationDelegate.h"
#import "ListViewController.h"
#import "ASLHelper.h"

@implementation ApplicationDelegate

@synthesize window = _window;

- ( void )dealloc
{
    [ _listController       release ];
    [ _navigationController release ];
    [ _window               release ];
    
    [ super dealloc ];
}

- ( BOOL )application: ( UIApplication * )application didFinishLaunchingWithOptions: ( NSDictionary * )launchOptions
{
    ( void )application;
    ( void )launchOptions;
    
    [ ASLHelper sharedInstance ];
    
    _listController            = [ ListViewController new ];
    _navigationController      = [ [ UINavigationController alloc ] initWithRootViewController: _listController ];
    _window                    = [ [ UIWindow alloc ] initWithFrame: [ [ UIScreen mainScreen ] bounds ] ];
    _window.rootViewController = _navigationController;
    
    _navigationController.navigationBar.tintColor = [ UIColor colorWithHue: ( CGFloat )0.59 saturation: ( CGFloat )0.1 brightness: ( CGFloat )0.65 alpha: ( CGFloat )1 ];
    
    [ self.window makeKeyAndVisible ];
    
    return YES;
}

@end
